struct Properties
{
	struct Property
	{
		p2SString name;
		int value;
	};

	~Properties()
	{
		p2List_item<Property*>* item;
		item = list.start;

		while (item != NULL)
		{
			RELEASE(item->data);
			item = item->next;
		}

		list.clear();
	}

	int Get(const char* name, int default_value = 0) const;

	p2List<Property*>	list;
};
struct Layer
{
	p2SString name = nullptr;
	uint width = 0;
	uint height = 0;
	uint* data = nullptr;
	float speed = 0.0f;
	Properties	properties;

	uint Get(int x, int y) const
	{
		return data[width*y + x];;
	}
};

